// ─── set_sprite_ptr ──────────────────────────────────────────
// In:  A = doodle index (0-7)
// Out: ZP_PTR_LO/HI = sprite_data + index * 63
//
// A * 63 = A * 64 - A
//   A*64: hi = A >> 2,  lo = (A & 3) << 6
//   Then subtract A (8-bit) from the 16-bit result.
set_sprite_ptr:
    sta ZP_TMP          // save index 0-7

    // Compute A*64 as 16-bit value in ZP_SPR_LO/HI
    lsr                 // A >> 1
    lsr                 // A >> 2
    sta ZP_SPR_HI       // hi = A >> 2  (max 1 for index 7)

    lda ZP_TMP
    and #3              // low 2 bits of index
    asl
    asl
    asl
    asl
    asl
    asl                 // (index & 3) << 6
    sta ZP_SPR_LO       // lo = (index & 3) << 6  (max $C0)

    // Subtract index from 16-bit A*64
    lda ZP_SPR_LO
    sec
    sbc ZP_TMP
    sta ZP_SPR_LO
    lda ZP_SPR_HI
    sbc #0
    sta ZP_SPR_HI

    // Add sprite_data base address
    lda ZP_SPR_LO
    clc
    adc #<sprite_data
    sta ZP_PTR_LO
    lda ZP_SPR_HI
    adc #>sprite_data
    sta ZP_PTR_HI
    rts
