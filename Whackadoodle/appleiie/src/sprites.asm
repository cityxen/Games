// ============================================================
// Apple IIe Doodle Sprites — converted from C64 originals
//
// C64 sprites: 24 px wide × 21 rows, 3 bytes/row, bit7 = leftmost px
// HGR output:  28 px wide × 21 rows, 4 bytes/row, bit0 = leftmost px
//              bit7 of each HGR byte = palette select (1 = blue/green)
//
// Pixel mapping: 24 C64 pixels split into 4 groups of 7 → 4 HGR bytes.
// Group 0: C64 px 0-6   → HGR byte 0  (px 21-23 of last group zero-padded)
// Bit order reversed per byte (C64 MSB-first ↔ HGR LSB-first).
//
// 4 bytes/row × 21 rows = 84 bytes per sprite.
// sprite_data + doodle*84 = base address.
//
// Doodle indices (same as C64):
//   0 = Happyface (good)   4 = RAD symbol (bad)
//   1 = Yin-yang  (good)   5 = Skull      (bad)
//   2 = Heart     (good)   6 = Poo pile   (bad)
//   3 = Star      (good)   7 = Frown      (bad)
// ============================================================

// ─── Assembly-time pixel conversion ──────────────────────────
// hgr_row(b0, b1, b2): emit 4 HGR bytes for one C64 sprite row.
//
// C64 bit order: bit7 = leftmost pixel within each byte.
// HGR bit order: bit0 = leftmost pixel, bit7 = palette (always 1).
//
// The 24 C64 pixels map to four 7-pixel HGR bytes:
//   HGR byte 0: C64 px 0-6   = b0[7..1]
//   HGR byte 1: C64 px 7-13  = b0[0], b1[7..2]
//   HGR byte 2: C64 px 14-20 = b1[1..0], b2[7..3]
//   HGR byte 3: C64 px 21-23 = b2[2..0]  (bits 3-6 = 0)
.macro hgr_row(b0, b1, b2) {
    // byte 0: px 0-6 (b0 bits 7..1, bit-reversed into HGR bit 0..6)
    .byte ((b0>>7)&1)      | (((b0>>6)&1)<<1) | (((b0>>5)&1)<<2) | (((b0>>4)&1)<<3) | (((b0>>3)&1)<<4) | (((b0>>2)&1)<<5) | (((b0>>1)&1)<<6)
    // byte 1: px 7-13
    .byte ((b0>>0)&1)      | (((b1>>7)&1)<<1) | (((b1>>6)&1)<<2) | (((b1>>5)&1)<<3) | (((b1>>4)&1)<<4) | (((b1>>3)&1)<<5) | (((b1>>2)&1)<<6)
    // byte 2: px 14-20
    .byte ((b1>>1)&1)      | (((b1>>0)&1)<<1) | (((b2>>7)&1)<<2) | (((b2>>6)&1)<<3) | (((b2>>5)&1)<<4) | (((b2>>4)&1)<<5) | (((b2>>3)&1)<<6)
    // byte 3: px 21-23 (4 trailing pixels are zero-padded)
    .byte ((b2>>2)&1)      | (((b2>>1)&1)<<1) | (((b2>>0)&1)<<2)
}

