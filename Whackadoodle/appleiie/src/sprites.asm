// ─── Sprite address lookup ────────────────────────────────────

.macro LoadSpritePtr(n) {
    lda #<[sprite_data + n*84]
    sta ZP_PTR_LO
    lda #>[sprite_data + n*84]
    sta ZP_PTR_HI
}

// Runtime: A = doodle index (0-7). Sets ZP_PTR_LO/HI.
// Computes A*84 = A*64 + A*16 + A*4 as a 16-bit value.
// Uses ZP_TMP, ZP_TMP2, ZP_SPR_LO, ZP_SPR_HI as scratch.
set_sprite_ptr:
    sta ZP_TMP              // save index (0-7)

    // A*4 (max 28, no overflow)
    asl
    asl
    sta ZP_SPR_LO
    lda #0
    sta ZP_SPR_HI

    // A*16 (max 112), add to running total
    lda ZP_TMP
    asl
    asl
    asl
    asl
    clc
    adc ZP_SPR_LO
    sta ZP_SPR_LO
    bcc ssp_no_c1
    inc ZP_SPR_HI
ssp_no_c1:

    // A*64: hi = A>>2, lo = (A&3)<<6  (no overflow on (A&3)<<6)
    lda ZP_TMP
    lsr
    lsr
    clc
    adc ZP_SPR_HI
    sta ZP_SPR_HI

    lda ZP_TMP
    and #3
    asl
    asl
    asl
    asl
    asl
    asl
    clc
    adc ZP_SPR_LO
    sta ZP_SPR_LO
    bcc ssp_no_c2
    inc ZP_SPR_HI
ssp_no_c2:

    // Add sprite_data base address
    lda ZP_SPR_LO
    clc
    adc #<sprite_data
    sta ZP_PTR_LO
    lda ZP_SPR_HI
    adc #>sprite_data
    sta ZP_PTR_HI
    rts
